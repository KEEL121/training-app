/**
 * SVGアイコン要素を返す
 * @param {keyof typeof PATHS | string} name
 * @param {number} [size]
 */
export function icon(name, size = 22) {
  const span = document.createElement('span');
  span.dataset.icon = String(name);
  span.style.display = 'inline-flex';
  // PATHSは静的定数のみ(ユーザー入力は入らない)のためinnerHTML使用可
  span.innerHTML = `<svg viewBox="0 0 24 24" width="${size}" height="${size}" aria-hidden="true">${PATHS[name] || ''}</svg>`;
  return span;
}

/** index.htmlの静的プレースホルダ(data-icon属性)にアイコンを差し込む */
export function hydrateStaticIcons() {
  document.querySelectorAll('#mainnav [data-icon], #fab [data-icon]').forEach((node) => {
    const name = node.getAttribute('data-icon');
    if (name && PATHS[name]) {
      node.innerHTML = `<svg viewBox="0 0 24 24" aria-hidden="true">${PATHS[name]}</svg>`;
    }
  });
}
